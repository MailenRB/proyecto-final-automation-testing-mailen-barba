import requests
from utils.data_reader import load_json_data
from utils.logger import get_logger

# Set up API logger
logger = get_logger("API_Tests")

# Load configuration data
data = load_json_data()
BASE_URL = data["api_url"]

def test_get_users():
    """Test 1: GET request - List users and validate structure."""
    logger.info("--- STARTING TEST: test_get_users ---")
    url = f"{BASE_URL}/users"
    logger.info(f"Sending GET request to: {url}")
    response = requests.get(url)
    
    logger.info(f"Response status received: {response.status_code}")
    assert response.status_code == 200, f"Expected 200, got {response.status_code}"
    
    res_json = response.json()
    
    logger.info("Verifying response is a list and contains items")
    assert isinstance(res_json, list), "Expected response to be a list of users"
    assert len(res_json) > 0, "Expected user list to not be empty"
    
    logger.info("Verifying keys on first user item")
    first_user = res_json[0]
    expected_keys = ["id", "name", "username", "email", "address"]
    for key in expected_keys:
        assert key in first_user, f"User item missing key '{key}'"
    logger.info("--- TEST PASSED: test_get_users ---")

def test_create_user():
    """Test 2: POST request - Create a user and validate response."""
    logger.info("--- STARTING TEST: test_create_user ---")
    url = f"{BASE_URL}/users"
    payload = {
        "name": "Mailen Barba",
        "username": "mailen",
        "email": "mailen@example.com"
    }
    logger.info(f"Sending POST request to: {url} with payload: {payload}")
    response = requests.post(url, json=payload)
    
    logger.info(f"Response status received: {response.status_code}")
    assert response.status_code == 201, f"Expected 201, got {response.status_code}"
    
    res_json = response.json()
    logger.info("Verifying user fields match request payload")
    assert res_json["name"] == payload["name"], f"Expected name '{payload['name']}', got '{res_json['name']}'"
    assert res_json["username"] == payload["username"], f"Expected username '{payload['username']}', got '{res_json['username']}'"
    assert res_json["email"] == payload["email"], f"Expected email '{payload['email']}', got '{res_json['email']}'"
    
    logger.info(f"Verifying autogenerated field 'id': {res_json.get('id')}")
    assert "id" in res_json, "Response missing autogenerated 'id'"
    logger.info("--- TEST PASSED: test_create_user ---")

